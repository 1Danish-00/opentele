def SimplifyDeviceModel(text : str):
    text.replace("_", "")

    for i in range(len(text)):
        
        if ord(text[i]) < 32:
            text[i] = ' '

    return " ".join(text.split())

def SimplifyGoodDeviceModel(text : str, limit : int, remove : typing.List[str]):
    words = text.split(' ')
    result = ""
    for word in words:

        if word.lower() in remove:
            continue
        elif result == "":
            result = word
        elif len(result) + len(word) < limit:
            result += ' ' + word
    
    return result
    

